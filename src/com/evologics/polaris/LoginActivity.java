	/**
	 * Attempts to sign in or register the account specified by the login form.
	 * If there are form errors (invalid email, missing fields, etc.), the
	 * errors are presented and no actual login attempt is made.
	 */
	public void attemptLogin() {
		if (mAuthTask != null) {
			return;
		}

		// Reset errors.
		mEmailView.setError(null);
		mPasswordView.setError(null);

		// Store values at the time of the login attempt.
		mEmail = mEmailView.getText().toString();
		mPassword = mPasswordView.getText().toString();

		boolean cancel = false;
		View focusView = null;

		// Check for a valid password.
		if (TextUtils.isEmpty(mPassword)) {
			mPasswordView.setError(getString(R.string.error_field_required));
			focusView = mPasswordView;
			cancel = true;
		} else if (mPassword.length() < 4) {
			mPasswordView.setError(getString(R.string.error_invalid_password));
			focusView = mPasswordView;
			cancel = true;
		}

		// Check for a valid email address.
		if (TextUtils.isEmpty(mEmail)) {
			mEmailView.setError(getString(R.string.error_field_required));
			focusView = mEmailView;
			cancel = true;
		} else if (!mEmail.contains("@")) {
			mEmailView.setError(getString(R.string.error_invalid_email));
			focusView = mEmailView;
			cancel = true;
		}

		if (cancel) {
			// There was an error; don't attempt login and focus the first
			// form field with an error.
			focusView.requestFocus();
		} else {
			// Show a progress spinner, and kick off a background task to
			// perform the user login attempt.
			mLoginStatusMessageView.setText(R.string.login_progress_signing_in);
			showProgress(true);
			mAuthTask = new UserLoginTask();
			mAuthTask.execute((Void) null);
		}
	}

	/**
	 * Shows the progress UI and hides the login form.
	 */
	@TargetApi(Build.VERSION_CODES.HONEYCOMB_MR2)
	private void showProgress(final boolean show) {
		// On Honeycomb MR2 we have the ViewPropertyAnimator APIs, which allow
		// for very easy animations. If available, use these APIs to fade-in
		// the progress spinner.
		if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.HONEYCOMB_MR2) {
			int shortAnimTime = getResources().getInteger(
					android.R.integer.config_shortAnimTime);

			mLoginStatusView.setVisibility(View.VISIBLE);
			mLoginStatusView.animate().setDuration(shortAnimTime)
					.alpha(show ? 1 : 0)
					.setListener(new AnimatorListenerAdapter() {
						@Override
						public void onAnimationEnd(Animator animation) {
							mLoginStatusView.setVisibility(show ? View.VISIBLE
									: View.GONE);
						}
					});

			mLoginFormView.setVisibility(View.VISIBLE);
			mLoginFormView.animate().setDuration(shortAnimTime)
					.alpha(show ? 0 : 1)
					.setListener(new AnimatorListenerAdapter() {
						@Override
						public void onAnimationEnd(Animator animation) {
							mLoginFormView.setVisibility(show ? View.GONE
									: View.VISIBLE);
						}
					});
		} else {
			// The ViewPropertyAnimator APIs are not available, so simply show
			// and hide the relevant UI components.
			mLoginStatusView.setVisibility(show ? View.VISIBLE : View.GONE);
			mLoginFormView.setVisibility(show ? View.GONE : View.VISIBLE);
		}
	}

	/**
	 * Represents an asynchronous login/registration task used to authenticate
	 * the user.
	 */
	public class UserLoginTask extends AsyncTask<Void, Void, Boolean> {
		@Override
		protected Boolean doInBackground(Void... params) {
			// TODO: attempt authentication against a network service.
			
			//Generate JSON String
			JSONObject JSONObj = generateJSON(mEmail, mPassword);
			String responseBody = "";
			try {
				int TIMEOUT_MILLISEC = 10000;  // = 10 seconds
				HttpParams httpParams = new BasicHttpParams();
				HttpConnectionParams.setConnectionTimeout(httpParams, TIMEOUT_MILLISEC);
				HttpConnectionParams.setSoTimeout(httpParams, TIMEOUT_MILLISEC);
				HttpClient client = new DefaultHttpClient(httpParams);

				HttpPost request = new HttpPost("https://polaris-app.herokuapp.com/api/login");
				request.setEntity( new StringEntity(JSONObj.toString()) );
				request.setHeader("Accept", "application/json");
				request.setHeader("Content-type", "application/json");
				
				ResponseHandler responseHandler = new BasicResponseHandler();
							
				HttpResponse response = client.execute(request);
				
				String body = this.getResponseBody(response);
				
				Log.d("response ->", body);
				responseBody = body;
				//Toast.makeText(null, response.toString(), Toast.LENGTH_LONG).show();
				
			} catch (UnsupportedEncodingException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			} catch (ClientProtocolException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			} catch (IOException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			}
			if (!responseBody.isEmpty()){
				try {
					JSONObject response = new JSONObject(responseBody);
					boolean success = (boolean) response.getBoolean("success");
					if(success){
						runOnUiThread(new Runnable(){
							public void run(){
								Toast.makeText(getApplicationContext(), "Successful Login", Toast.LENGTH_LONG).show();
							}
						});
					}else{
						runOnUiThread(new Runnable(){
							public void run(){
								Toast.makeText(getApplicationContext(), "Not a Successful Login", Toast.LENGTH_LONG).show();
							}
						});
					}
				} catch (JSONException e) {
					// TODO Auto-generated catch block
					Log.d("exception", e.getMessage());
				}
				
			}
			for (String credential : DUMMY_CREDENTIALS) {
				String[] pieces = credential.split(":");
				if (pieces[0].equals(mEmail)) {
					// Account exists, return true if the password matches.
					return pieces[1].equals(mPassword);
				}
			}

			// TODO: register the new account here.
			return true;
		}

		private String getResponseBody(HttpResponse response) {

			String response_text = null;

			HttpEntity entity = null;

			try {

				entity = response.getEntity();

				response_text = _getResponseBody(entity);

			} catch (ParseException e) {

				e.printStackTrace();

			} catch (IOException e) {

				if (entity != null) {

					try {

						entity.consumeContent();

					} catch (IOException e1) {

					}

				}

			}

			return response_text;

		}

		private String _getResponseBody(final HttpEntity entity)
				throws IOException, ParseException {

			if (entity == null) {
				throw new IllegalArgumentException(
						"HTTP entity may not be null");
			}

			InputStream instream = entity.getContent();

			if (instream == null) {
				return "";
			}

			if (entity.getContentLength() > Integer.MAX_VALUE) {
				throw new IllegalArgumentException(

				"HTTP entity too large to be buffered in memory");
			}

			String charset = getContentCharSet(entity);

			if (charset == null) {

				charset = HTTP.DEFAULT_CONTENT_CHARSET;

			}

			Reader reader = new InputStreamReader(instream, charset);

			StringBuilder buffer = new StringBuilder();

			try {

				char[] tmp = new char[1024];

				int l;

				while ((l = reader.read(tmp)) != -1) {

					buffer.append(tmp, 0, l);

				}

			} finally {

				reader.close();

			}

			return buffer.toString();

		}

		public String getContentCharSet(final HttpEntity entity)
				throws ParseException {

			if (entity == null) {
				throw new IllegalArgumentException(
						"HTTP entity may not be null");
			}

			String charset = null;

			if (entity.getContentType() != null) {

				HeaderElement values[] = entity.getContentType().getElements();

				if (values.length > 0) {

					NameValuePair param = values[0]
							.getParameterByName("charset");

					if (param != null) {

						charset = param.getValue();

					}

				}

			}

			return charset;

		}

		private JSONObject generateJSON(String email, String password) {
			JSONObject user_login = new JSONObject();
			JSONObject credentials = new JSONObject();
			try {

				credentials.put("password", password);
				credentials.put("email", email);
				user_login.put("user_login", credentials);
				Log.d("JSON String is: ", user_login.toString());
				return user_login;
			} catch (JSONException e) {
				e.printStackTrace();
			}
			return null;
		}

		@Override
		protected void onPostExecute(final Boolean success) {
			mAuthTask = null;
			showProgress(false);

			if (success) {
				finish();
			} else {
				mPasswordView
						.setError(getString(R.string.error_incorrect_password));
				mPasswordView.requestFocus();
			}
		}

		@Override
		protected void onCancelled() {
			mAuthTask = null;
			showProgress(false);
		}
	}
